#!/usr/bin/env python3
"""
gen_missing_stubs.py — Generate .cpp stub bodies for newly declared functions
and mark class members as hook.translated=true in functions.json.

Part A: Reads src/_generated/named_free_decls.hpp and sub_decls.hpp →
generates named_free_stubs.cpp and sub_stubs.cpp with minimal return-0 stubs.

Part B (--mark-members): Reads functions.json, marks eligible class members
as hook.translated=true (skipping CRT exclusions).

Idempotent: re-running produces identical output (sorted by function name).

Usage:
  python tools/gen_missing_stubs.py                    # full stub generation
  python tools/gen_missing_stubs.py --dry-run          # print to stdout only
  python tools/gen_missing_stubs.py --sample 10        # sample first 10 funcs
  python tools/gen_missing_stubs.py --mark-members     # mark members in functions.json
  python tools/gen_missing_stubs.py --mark-members --dry-run  # preview member marks
"""

import json
import os
import re
import sys
import argparse
from pathlib import Path
from collections import OrderedDict

PROJECT_ROOT = Path(__file__).parent.parent
SRC_GEN = PROJECT_ROOT / "src" / "_generated"
NAMED_DECLS = SRC_GEN / "named_free_decls.hpp"
SUB_DECLS = SRC_GEN / "sub_decls.hpp"
NAMED_STUBS_OUT = SRC_GEN / "named_free_stubs.cpp"
SUB_STUBS_OUT = SRC_GEN / "sub_stubs.cpp"
FUNCTIONS_JSON = PROJECT_ROOT / "injectFunctionTest" / "functions.json"
CRT_EXCL_JSON = PROJECT_ROOT / "tools" / "fr_crt_exclusions.json"

# ── Part A: Stub generation ─────────────────────────────────────────────────

DECL_PATTERN = re.compile(
    r'^\s*'
    r'(.+?)\s+'                 # return type
    r'(__stdcall|__fastcall|__cdecl)\s+'  # calling convention
    r'(\w+)\s*'                 # function name
    r'\(([^)]*)\)\s*'           # params
    r';\s*//\s*(0x[0-9A-Fa-f]+)'  # address comment
)

# Lines that aren't function declarations (comments, pragma, blank, include)
SKIP_LINE = re.compile(r'^\s*(#|//|$)')


def parse_declaration(line: str):
    """Parse one declaration line. Returns dict or None if not a decl."""
    m = DECL_PATTERN.match(line)
    if not m:
        return None
    ret_type = m.group(1).strip()
    conv = m.group(2)
    func_name = m.group(3)
    params = m.group(4).strip()
    addr = m.group(5)
    return {
        'ret': ret_type,
        'conv': conv,
        'name': func_name,
        'params': params,
        'addr': addr,
    }


def is_void_return(ret: str) -> bool:
    """Check if return type is plain void (not void*)."""
    return ret.strip() == 'void'


def format_stub(parsed: dict) -> str:
    """Format one stub function body."""
    ret = parsed['ret']
    conv = parsed['conv']
    name = parsed['name']
    params = parsed['params']
    addr = parsed['addr']

    sig = f'{ret} {conv} {name}({params})'

    if is_void_return(ret):
        body = '{\n}'
    else:
        body = '{\n    return 0;\n}'

    stub = f'{sig}\n{body}'
    return stub


def parse_hpp_file(filepath: Path) -> list:
    """Parse an hpp file and return list of parsed declarations."""
    try:
        content = filepath.read_text(encoding='utf-8')
    except UnicodeDecodeError:
        content = filepath.read_text(encoding='utf-8-sig')

    decls = []
    for line in content.splitlines():
        if SKIP_LINE.match(line):
            continue
        parsed = parse_declaration(line)
        if parsed:
            decls.append(parsed)
    return decls


def generate_stub_cpp(decls: list, include_file: str) -> str:
    """Generate a complete .cpp file from parsed declarations."""
    # Sort by function name for determinism
    decls.sort(key=lambda d: d['name'].lower())

    lines = [
        f'#include "{include_file}"',
        '',
        f'// Auto-generated by gen_missing_stubs.py',
        f'// {len(decls)} stubs',
        '// DO NOT EDIT MANUALLY',
        '',
        '#pragma warning(disable: 4100 4189)',
        '',
    ]

    for d in decls:
        stub = format_stub(d)
        lines.append(stub)
        lines.append('')

    return '\n'.join(lines)


def run_stub_generation(args):
    """Part A: generate stub .cpp files."""
    print(f'Parsing declarations...', file=sys.stderr)

    named_decls = parse_hpp_file(NAMED_DECLS)
    sub_decls = parse_hpp_file(SUB_DECLS)

    print(f'  named_free_decls.hpp: {len(named_decls)} functions', file=sys.stderr)
    print(f'  sub_decls.hpp: {len(sub_decls)} functions', file=sys.stderr)

    if args.sample:
        named_decls = named_decls[:args.sample]
        sub_decls = sub_decls[:args.sample]
        print(f'  Sampled: {len(named_decls)} + {len(sub_decls)}', file=sys.stderr)

    named_output = generate_stub_cpp(named_decls, 'named_free_decls.hpp')
    sub_output = generate_stub_cpp(sub_decls, 'sub_decls.hpp')

    if args.dry_run:
        print('=== named_free_stubs.cpp ===')
        print(named_output)
        print('=== sub_stubs.cpp ===')
        print(sub_output)
    else:
        SRC_GEN.mkdir(parents=True, exist_ok=True)
        NAMED_STUBS_OUT.write_text(named_output + '\n', encoding='utf-8', newline='\n')
        print(f'Wrote {NAMED_STUBS_OUT} ({len(named_decls)} stubs)', file=sys.stderr)
        SUB_STUBS_OUT.write_text(sub_output + '\n', encoding='utf-8', newline='\n')
        print(f'Wrote {SUB_STUBS_OUT} ({len(sub_decls)} stubs)', file=sys.stderr)


# ── Part B: Member marking ───────────────────────────────────────────────────

def load_crt_exclusions() -> set:
    """Load CRT exclusion addresses as a set of normalized hex strings."""
    with open(CRT_EXCL_JSON, 'r', encoding='utf-8') as f:
        crt_data = json.load(f)
    return set(addr.upper() for addr in crt_data['excluded_addresses'])


def mark_members(args):
    """Part B: mark class member functions as hook.translated=true in functions.json."""
    print(f'Loading functions.json...', file=sys.stderr)

    with open(FUNCTIONS_JSON, 'r', encoding='utf-8') as f:
        fj = json.load(f)

    crt_set = load_crt_exclusions()
    print(f'  CRT exclusions: {len(crt_set)} addresses', file=sys.stderr)

    already_translated = 0
    newly_marked = 0
    skipped_crt = 0
    skipped_no_class = 0

    functions = fj['functions']

    for fentry in functions:
        call = fentry.get('call', {}) or {}

        # Only class members with known class_name
        if not call.get('is_member', False):
            continue
        class_name = call.get('class_name')
        if not class_name:
            skipped_no_class += 1
            continue

        # Skip CRT exclusions
        addr = fentry.get('address', '').upper()
        if addr in crt_set:
            skipped_crt += 1
            continue

        # Mark as translated
        hook = fentry.get('hook', {})
        if hook.get('translated', False):
            already_translated += 1
        else:
            if not args.dry_run:
                hook['translated'] = True
            newly_marked += 1

    print(f'  Already translated: {already_translated}', file=sys.stderr)
    print(f'  Newly marked: {newly_marked}', file=sys.stderr)
    print(f'  Skipped (CRT): {skipped_crt}', file=sys.stderr)
    print(f'  Skipped (no class_name): {skipped_no_class}', file=sys.stderr)
    print(f'  Total class members: {already_translated + newly_marked + skipped_crt + skipped_no_class}', file=sys.stderr)

    if not args.dry_run:
        # Create backup
        bak_path = FUNCTIONS_JSON.with_suffix('.json.bak')
        print(f'  Creating backup: {bak_path}', file=sys.stderr)
        with open(bak_path, 'w', encoding='utf-8', newline='\n') as f:
            json.dump(fj, f, indent=2, ensure_ascii=False)
            f.write('\n')

        # Write updated functions.json preserving structure
        print(f'  Writing {FUNCTIONS_JSON}', file=sys.stderr)
        with open(FUNCTIONS_JSON, 'w', encoding='utf-8', newline='\n') as f:
            json.dump(fj, f, indent=2, ensure_ascii=False)
            f.write('\n')
    else:
        print(f'  (dry-run: no files written)', file=sys.stderr)


# ── Main ─────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description='Generate stub bodies and/or mark class members as translated'
    )
    parser.add_argument('--dry-run', action='store_true',
                        help='Print to stdout instead of writing files')
    parser.add_argument('--sample', type=int, default=None,
                        help='Only process first N functions (for testing)')
    parser.add_argument('--mark-members', action='store_true',
                        help='Mark class member functions as hook.translated=true in functions.json')
    args = parser.parse_args()

    if not args.mark_members:
        # Part A: stub generation (default)
        run_stub_generation(args)
    else:
        # Part B: member marking
        mark_members(args)


if __name__ == '__main__':
    main()
